    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        int response = JOptionPane.showConfirmDialog(rootPane,"Tem certeza que deseja excluir?");
        
        switch (response) {
            case 0:
                Integer id = (Integer) jTable1.getValueAt(jTable1.getSelectedRow(),0);
                em.delete(id);
                jUser.setEnabled(false);
                txtSalary.setEnabled(false);
                admin.setEnabled(false);
                jUser.setSelectedIndex(-1);
                txtSalary.setText("");
                admin.setSelected(false);
                btnCancel.setEnabled(false);
                btnSave.setEnabled(false);
                btnDelete.setEnabled(false);
                btnEdit.setEnabled(false);
                btnNew.setEnabled(true);
                table();
                break;
            case 1:
                JOptionPane.showMessageDialog(rootPane,"Você cancelou");
                break;
            default:
                JOptionPane.showMessageDialog(rootPane,"Você cancelou");
                break;
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

