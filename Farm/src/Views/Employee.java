    private void table(){
        
        ArrayList data = new ArrayList();
        String[] colunm  = new String[]{"Código","Nome","Salário","Admin"};
        
       
        List<Classes.Employee> ems = null;
        
        try {
            ems = em.getEmployees();
            Iterator<Classes.Employee> e = ems.iterator();
            while(e.hasNext()){
                Classes.Employee client = e.next();
                data.add(new Object[]{client.getId(),client.getName(),client.getSalary(),client.getAdmin()});
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Employee.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        ModelTable table = new ModelTable(data, colunm);
        jTable1.setModel(table);
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTable1.getColumnModel().getColumn(0).setResizable(false);
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(195);
        jTable1.getColumnModel().getColumn(1).setResizable(false);
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(195);
        jTable1.getColumnModel().getColumn(2).setResizable(false);
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(70);
        jTable1.getColumnModel().getColumn(3).setResizable(false);
        jTable1.getTableHeader().setResizingAllowed(false);
        jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_OFF);
        jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
