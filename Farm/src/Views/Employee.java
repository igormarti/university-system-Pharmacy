    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
      
      if(jUser.getSelectedIndex() == -1){
          JOptionPane.showMessageDialog(rootPane,"É necessário selecionar um usuário.");
      }else if(txtSalary.getText().isEmpty()){
          JOptionPane.showMessageDialog(rootPane,"O campo ' Salário' é necessário.");
      }else{
         Classes.Employee e = new Classes.Employee();
         String name_user;
         Integer id_user;
       
         if(this.flag == 1){
            
            name_user = (String) jUser.getSelectedItem();
            id_user = getIdUser(name_user);
            e.setId_user(id_user);
            
           
            e.setSalary(Double.parseDouble(txtSalary.getText()));
            
            Integer a = (admin.isSelected())?1:0;
            e.setAdmin(a);

             try {
                 em.Save(e);
             } catch (SQLException | ClassNotFoundException ex) {
                 Logger.getLogger(Employee.class.getName()).log(Level.SEVERE, null, ex);
             }

             jUser.setEnabled(false);
             txtSalary.setEnabled(false);
             admin.setEnabled(false);
             jUser.setSelectedIndex(-1);
             txtSalary.setText("");
             admin.setSelected(false);
             btnCancel.setEnabled(false);
             btnSave.setEnabled(false);
             btnDelete.setEnabled(false);
             btnEdit.setEnabled(false);
             btnNew.setEnabled(true);
             table();
             
           }else{
             name_user = (String) jUser.getSelectedItem();
             id_user = getIdUser(name_user);
             Integer id = (Integer) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            
             e.setId(id);
             e.setId_user(id_user);
             e.setSalary(Double.parseDouble(txtSalary.getText()));
             Integer a = (admin.isSelected())?1:0;
             e.setAdmin(a);
             
             try {
                 em.Edit(e);
             } catch (SQLException | ClassNotFoundException ex) {
                 Logger.getLogger(Employee.class.getName()).log(Level.SEVERE, null, ex);
             }
           } 
         
             jUser.setEnabled(false);
             txtSalary.setEnabled(false);
             admin.setEnabled(false);
             jUser.setSelectedIndex(-1);
             txtSalary.setText("");
             admin.setSelected(false);
             btnCancel.setEnabled(false);
             btnSave.setEnabled(false);
             btnDelete.setEnabled(false);
             btnEdit.setEnabled(false);
             btnNew.setEnabled(true);
             table();
           
      }
        
    }//GEN-LAST:event_btnSaveActionPerformed

