    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
      
      if(jType.getSelectedIndex() == -1){
          JOptionPane.showMessageDialog(rootPane,"É necessário selecionar um usuário.");
      }else if(txtname.getText().isEmpty()){
          JOptionPane.showMessageDialog(rootPane,"O campo 'Nome' é necessário.");
      }
      else if(txtPrice.getText().isEmpty()){
          JOptionPane.showMessageDialog(rootPane,"O campo 'Preço' é necessário.");
      }else{
         Classes.Product pro = new Classes.Product();
        
       
         if(this.flag == 1){
           pro.setName(txtname.getText());
           pro.setDescribe(txtDescribe.getText());
           pro.setPrice(Float.parseFloat(txtPrice.getText()));
           pro.setExpiration(jExpiration.getDate());
           pro.setType((jType.getSelectedIndex()));
           pro.setQuantity(Integer.parseInt(txtQtd.getText()));
             try {
                 p.Save(pro);
             } catch (SQLException | ClassNotFoundException ex) {
                 Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
             }

               jType.setEnabled(false);
                txtname.setEnabled(false);
                txtDescribe.setEnabled(false);
                txtPrice.setEnabled(false);
                jExpiration.setEnabled(false);
                jType.setSelectedIndex(0);
                txtQtd.setEnabled(false);
                txtQtd.setText("");
                txtname.setText("");
                txtDescribe.setText("");
                txtPrice.setText("");
                btnCancel.setEnabled(false);
                btnSave.setEnabled(false);
                btnDelete.setEnabled(false);
                btnEdit.setEnabled(false);
                btnNew.setEnabled(true);
                table();
             
           }else{
            
                Integer id = (Integer) jTable.getValueAt(jTable.getSelectedRow(), 0);
                pro.setId(id);
                pro.setName(txtname.getText());
                pro.setDescribe(txtDescribe.getText());
                pro.setPrice(Float.parseFloat(txtPrice.getText()));
                pro.setExpiration(jExpiration.getDate());
                pro.setType((jType.getSelectedIndex()));
                pro.setQuantity(Integer.parseInt(txtQtd.getText()));
             
             try {
                 p.Edit(pro);
             } catch (SQLException | ClassNotFoundException ex) {
                 Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
             }
           } 
         
                jType.setEnabled(false);
                txtname.setEnabled(false);
                txtDescribe.setEnabled(false);
                txtPrice.setEnabled(false);
                jExpiration.setEnabled(false);
                jType.setSelectedIndex(0);
                txtQtd.setEnabled(false);
                txtQtd.setText("");
                txtname.setText("");
                txtDescribe.setText("");
                txtPrice.setText("");
                btnCancel.setEnabled(false);
                btnSave.setEnabled(false);
                btnDelete.setEnabled(false);
                btnEdit.setEnabled(false);
                btnNew.setEnabled(true);
                table();
           
      }
        
    }//GEN-LAST:event_btnSaveActionPerformed

