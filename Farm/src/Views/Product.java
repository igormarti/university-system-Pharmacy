    private void table(){
        
        ArrayList data = new ArrayList();
        String[] colunm  = new String[]{"Código","Nome","Validade","Preço","Tipo","Quantidade"};
        
        List<Classes.Product> products = null;
        
        try {
            products = p.getProducts();
            Iterator<Classes.Product> pro = products.iterator();
            while(pro.hasNext()){
                Classes.Product pr = pro.next();
                String Type = (pr.getType()==0)?"Remédio":"Higienico";
                data.add(new Object[]{pr.getId(),pr.getName(),pr.getExpiration(),pr.getPrice(),Type,pr.getQuantity()});
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        ModelTable table = new ModelTable(data, colunm);
        jTable.setModel(table);
        jTable.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTable.getColumnModel().getColumn(0).setResizable(false);
        jTable.getColumnModel().getColumn(1).setPreferredWidth(250);
        jTable.getColumnModel().getColumn(1).setResizable(false);
        jTable.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTable.getColumnModel().getColumn(2).setResizable(false);
        jTable.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTable.getColumnModel().getColumn(3).setResizable(false);
        jTable.getColumnModel().getColumn(4).setPreferredWidth(100);
        jTable.getColumnModel().getColumn(4).setResizable(false);
        jTable.getColumnModel().getColumn(5).setPreferredWidth(100);
        jTable.getColumnModel().getColumn(5).setResizable(false);
        jTable.getTableHeader().setResizingAllowed(false);
        jTable.setAutoResizeMode(jTable.AUTO_RESIZE_OFF);
        jTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
